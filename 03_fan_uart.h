#ifndef FAN_UART_H_
#define FAN_UART_H_
// ↑ Include Guard (중복 포함 방지): 
// 여러 파일에서 이 헤더를 부르더라도, 컴파일러가 중복해서 코드를 복사해 넣는 것을 막아주는 안전장치.

// 통신 모듈에서도 시스템 전체의 공통 상태(speed_state 등)를 알아야 하므로 공용 설계도를 가져옴.
#include "01_fan_config.h"

// -----------------------------------------------------------
// 1. 디코딩 파라미터 (명령어 셋 매크로)
// -----------------------------------------------------------
// 센서로 들어온 펄스들을 32비트로 조립한 뒤, 이 값들과 비교하여 어떤 버튼인지 판별함.
#define IR_BTN_SPEED_UP   0xEA15FF00  // 풍속 증가 (+)
#define IR_BTN_SPEED_DOWN 0xF807FF00  // 풍속 감소 (-)
#define IR_BTN_ROTATE     0xBF40FF00  // 회전 토글
#define IR_BTN_PLAYPAUSE  0xBC43FF00  // 재생/정지

// -----------------------------------------------------------
// 2. 통신 모듈 전용 상태 변수 (공유 변수 선언)
// -----------------------------------------------------------
// [개념 복습]
// * extern: "이 변수의 진짜 메모리(방)는 main.c에 이미 만들어져 있으니, 여기서는 선만 연결해서 쓸게!" 라는 뜻. (메모리 중복 할당 방지)
// * volatile: "인터럽트(하드웨어 신호) 때문에 값이 언제든 예고 없이 훅훅 바뀔 수 있으니, 컴파일러 너 맘대로 캐싱하거나 최적화(생략)하지 마!" 라는 강력한 경고.

extern volatile uint32_t ir_code;           // 조립이 끝난 32비트 리모컨 코드를 잠시 담아두는 버퍼
extern volatile uint8_t ir_flag;            // 수신 완료를 알리는 깃발 (1: 수신완료로 메인루프 개입, 0: 대기)
extern volatile uint8_t saved_speed_state;  // 재생/정지 기능을 위해 '직전 풍속(1~3)'을 래치(Latch)해두는 공간
extern volatile uint8_t saved_rotate_state; // 재생/정지 기능을 위해 '직전 회전 여부(0,1)'를 래치해두는 공간

// -----------------------------------------------------------
// 3. 외부 공개 API (함수 선언부)
// -----------------------------------------------------------
// fan_uart.c 내부에서만 몰래 쓰는 게 아니라, main.c 같은 '밖'에서도 부를 수 있게 이름표(Interface)를 열어둠.
void comm_init(void);        // 하드웨어 세팅 (UART 보드레이트, Timer3 분주비, INT4 하강 에지 설정 등)
void comm_process(void);     // 메인 루프(while) 안에서 계속 돌며 ir_flag를 감시하고, 실제 상태(State)를 변경하는 핵심 로직
void uart0_print(char *str); // 상태 모니터링을 위해 PC 터미널로 문자열을 쪼개서 쏴주는 함수

#endif /* FAN_UART_H_ */