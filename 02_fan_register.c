#include "01_fan_config.h"
#include "02_fan_register.h"

void register_init(void) {
    // ---------------------------------------------------
    // [입력 핀 설정] 버튼 및 센서 (방향: 0, 풀업: 1)
    // ---------------------------------------------------
    
    // 1. 본체 물리 버튼 2개 (PA0: 풍속, PA1: 회전)
    DDRA &= ~((1 << PA0) | (1 << PA1)); // 입력 모드
    PORTA |= (1 << PA0) | (1 << PA1);   // 내부 풀업 저항 켜기

    // 2. IR 리모컨 적외선 수신부 (PE4: 외부 인터럽트 4번 핀)
    DDRE &= ~(1 << PE4); // 입력 모드
    PORTE |= (1 << PE4); // 내부 풀업 저항 켜기

    // ---------------------------------------------------
    // [출력 핀 설정] 모터, FND, LED (방향: 1)
    // ---------------------------------------------------

    // 3. 모터 제어 핀 (PB5: 서보 모터 PWM, PB6: 팬 모터 PWM)
    DDRB |= (1 << PB5) | (1 << PB6); // 출력 모드

    // 4. 풍속 표시용 FND (포트 C 전체 8핀 사용)
    DDRC = 0xFF; // 출력 모드 (0xFF = 11111111)

    // 5. 회전 표시용 LED 바 (포트 D 전체 8핀 사용)
    DDRD = 0xFF; // 출력 모드 (0xFF = 11111111)
}

